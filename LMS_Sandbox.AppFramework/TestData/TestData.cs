        /// <summary>
        /// 
        /// </summary>
        /// <param name="allQandAs"></param>
        /// <param name="returnCorrectAnswers">'true' if you want to return the correct answers, false if you want to return the incorrect answers</param>
        /// <param name="activityTitle">The title of the activity. This activity title text should match one of the several activities 
        /// that are setup for PIMs. <see cref="PIMGroups"/> </param>
        /// <param name="assessmentName">The title of the assessment. This assessment title text should match one of the assessment titles
        /// inside one of the several activities that are setup for PIMs. <see cref="PIMPatients"/> </param>
        /// <param name="correctAnswers">(Optional). If you want to manually specify the correct answers for an assessment, pass this 
        /// parameter, else leave it blank and the automation will determine the correct answers based on the activity and 
        /// assessment title</param>
        public List<Constants.AssQAndAs> GetAnswerSet(List<Constants.AssQAndAs> allQandAs, bool returnCorrectAnswers = true,
            string activityTitle = null, string assessmentName = null)
        {
            List<Constants.AssQAndAs> incorrectAnswerSet = new List<Constants.AssQAndAs>();
            List<Constants.AssQAndAs> correctAnswerSet = new List<Constants.AssQAndAs>();
            List<string> answers = null;

            PIMGroups targetPimGroup = new PIMGroups();
            PIMPatients targetPatient = new PIMPatients();
            bool pimGroupFound = false;
            bool patientFound = false;

            // First, reset all answers to be false (later in this method, we set them to be true or false depending on the 
            // assessment/patient that the tester passed in the parameter
            foreach (var item in allQandAs)
            {
                item.IsCorrect = false;
            }

            // Set the pim group
            foreach (PIMGroups pimGroup in Enum.GetValues(typeof(PIMGroups)))
            {
                if (pimGroup.GetDescription() == activityTitle)
                {
                    targetPimGroup = pimGroup;
                    pimGroupFound = true;
                    break;
                }
            }

            // Set the patient
            foreach (PIMPatients patient in Enum.GetValues(typeof(PIMPatients)))
            {
                if (patient.GetDescription() == assessmentName)
                {
                    targetPatient = patient;
                    patientFound = true;
                    break;
                }
            }

            if (!pimGroupFound)
            {
                throw new Exception("Your activity title did not match any of the stored PIM groups inside the Enum PIMGroups. " +
                    "Please add your pim group to the Enum PIMGroups, and then add your answers to the GetAnswers method");
            }
            if (!patientFound)
            {
                throw new Exception("Your assessment title did not match any of the stored patients inside the Enum PIMPatients. " +
                    "Please add your patient to the Enum PIMGroups, and then add your answers to the GetAnswers method");
            }

            answers = GetCorrectAnswers(targetPimGroup, targetPatient);

            // Get the distinct questions (unique records/questions) based on question ID for the list of questions that the tester passed
            var questions = allQandAs.GroupBy(test => test.QuestionId).Select(grp => grp.First()).ToList();

            // Get the IDs of those distinct questions
            List<int> questionIDs = new List<int>();
            foreach (var item in questions)
            {
                questionIDs.Add(item.QuestionId);
            }

            // For each question
            for (int i = 0; i < questionIDs.Count; i++)
            {
                bool answerFound = false;

                // Get all answers within the question
                var answerSets = allQandAs.Where(ques => ques.QuestionId == questionIDs[i]);

                // For each answer, if the answerID matches the testers requested answerID, set it to true (They are all false by default). 
                // Then break
                foreach (var answerSet in answerSets)
                {
                    if (answers[i].Contains(answerSet.AnswerID))
                    //if (answerSet.AnswerID.Contains(answers[i]))
                    {
                        answerSet.IsCorrect = true;
                        answerFound = true;
                        //allQandAs.Select(x => { x.IsCorrect = true; return x; });
                    }
                }

                // Sometimes a question can have EVERY answer a correct answer. If that happens, and the tester requested that they wanted 
                // to return all incorrect answers, then we have to sacrifice and just set one of these correct answers to incorrect. 
                // Otherwise, when they go to submit the assessment, one of the questions will not get answered and so they will get a 
                // Required Answer error
                if (!answerSets.Any(answer => answer.IsCorrect == false))
                {
                    answerSets.ToList()[0].IsCorrect = false;
                }

                // After looping through the answers within the question, if the answer requested was not found, tell the tester
                if (answerFound == false)
                {
                    string questionText = questions[i].QuestionText;
                    throw new Exception("Your answer was not found within the question of: " + questionText);
                }

                correctAnswerSet.AddRange(answerSets.Where(q => q.IsCorrect == true).ToList());
                incorrectAnswerSet.AddRange(answerSets.Where(q => q.IsCorrect == false).ToList());
            }

            if (returnCorrectAnswers)
            {
                return correctAnswerSet;
            }
            else
            {
                return incorrectAnswerSet;
            }
        }

